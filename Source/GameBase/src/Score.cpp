#include "stdafx.h"
#include "Score.h"

using namespace GameBase;

// *****************************************************************************
cScore::cScore()
: m_iScore(0)
{
}

// *****************************************************************************
cScore::~cScore()
{
}

// *****************************************************************************
void cScore::UpdateScore(const int iDeltaValue)
{
	m_iScore += iDeltaValue;
}

// *****************************************************************************
int cScore::GetScore() const
{
	return m_iScore;
}

// *****************************************************************************
bool cScore::operator>(cScore const &s) const 
{ 
	return m_iScore > s.m_iScore; 
}